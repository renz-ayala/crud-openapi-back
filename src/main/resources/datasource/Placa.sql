CREATE USER SCHEMA_ IDENTIFIED BY "PASSWORD";

GRANT CONNECT, RESOURCE TO SCHEMA_;

ALTER USER SCHEMA_ QUOTA UNLIMITED ON USERS;

CREATE TABLE SCHEMA_.PLACA (
    id_placa        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    numero_placa    VARCHAR2(10) NOT NULL UNIQUE,
    marca           VARCHAR2(50),
    modelo          VARCHAR2(50),
    color           VARCHAR2(30),
    anio_fabricacion NUMBER(4),
    propietario_dni  VARCHAR2(15),
    estado          VARCHAR2(20) DEFAULT 'ACTIVO',
    fecha_registro  TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    observaciones   VARCHAR2(500),

    CONSTRAINT check_estado CHECK (estado IN ('ACTIVO', 'INACTIVO', 'ALERTA', 'ROBADO'))
);

